    public static void disableButton(){
        if((tfSenha1.getText().equals(tfSenha2.getText())) 
                & !(tfSenha1.getText().equals(""))
                & !(tfUsuario.getText().equals(""))){
            btCadastrar.setEnabled(true);
        }else{
            btCadastrar.setEnabled(false);
        }
    }
    
